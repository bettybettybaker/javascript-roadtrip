// Challenge:

// Enter one expression that satisfies all of the following criteria:

// - The answer evaluates to 14.
// - The expression contains at least 3 different values.
// - The expression contains exactly 1 set of parentheses.



///////////////////////// Solution /////////////////////////

17 - (58 % 11)

// Follows PEMDAS order of operations.
// modulus (%) gives the remainder and evaluates in the same order
// as multiplication in PEMDAS
// This evaluates as 58 % 11 = 3 (remainder of 58 / 11)
// then 17 - 3 = 14
