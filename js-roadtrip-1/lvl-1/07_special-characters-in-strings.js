// Challenge:

// Build the following output in one String, using special characters
// for the newline, tab stop, and quotation marks:

// Quoth the raven:
//    "Nevermore!"

///////////////////////// Solution /////////////////////////

"Quoth the raven:\n\t\"Nevermore!\""

//          \n = new line
//          \t = tab
//          \" = escapes the quote - allows for double quotes inside string
